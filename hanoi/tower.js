function Tower(x,y,w,h) {
	
	this.x = x;
	this.y = y;
	this.w = w;
	this.h = h;
	this.disks = [];
	
	this.show = function(){
		
		stroke(255);
		fill(0);
		rect(this.x,this.y,this.w,this.h);
	
	}
	
	
	
	
}